package modelos;

public class Morador {
    private String nome;
    private int idade;
    private char sexo;
    private String rg;
    private String cpf;
    private double renda;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public double getRenda() {
        return renda;
    }

    public void setRenda(double renda) {
        this.renda = renda;
    }

    @Override public String toString() { return "Nome: " + nome + "\nIdade: " + idade + "\nSexo: " + sexo + "\nRG: " + rg + "\nCPF: " + cpf + "\nRenda: " + renda; }
}
